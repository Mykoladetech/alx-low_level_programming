#include "main.h"
#include <stdio.h>
/**
 *	_memset - function that fill memory with constant
 *	@s: starting the adress of memory to be filled
 *	@b: the desired value
 *	@n: number of bytes to be changed
 *	Return: change an array with new value of new byte
 */
char *_memset(char *s, char b, unsigned int n)
{
	int i = 0;

	for (; n > 0; i++)
	{
		s[i] = b;
		n--;
	}
		return (s);
}
